    [Authorize(LFGPermissions.Prodotti.Edit)]
    public async Task<PrezzoNormalizzazioneResultDto> NormalizzaPrezziAsync()
    {
        var result = new PrezzoNormalizzazioneResultDto();

        var tutti = await _prodottoRepository.GetListAsync();
        result.TotaleEsaminati = tutti.Count;

        foreach (var prod in tutti)
        {
            var prima = prod.Prezzo;
            var dopo  = LFG.Helpers.PrezzoHelper.NormalizzaRaw(prima);

            // Anomalia: stringa non vuota che non produce un valore parsabile
            var anomalia   = dopo == prima && LFG.Helpers.PrezzoHelper.Parse(prima) == 0m
                             && !string.IsNullOrWhiteSpace(prima);
            var modificato = dopo != prima;

            result.Log.Add(new PrezzoConversioneLog
            {
                ProdottoId = prod.Id,
                Nome       = prod.Nome,
                PrimaDi    = prima,
                DopoDi     = dopo,
                Modificato = modificato,
                Anomalia   = anomalia,
            });

            if (anomalia)
            {
                result.TotaleAnomalie++;
                Logger.LogWarning(
                    "[NormalizzaPrezzi] ANOMALIA prodotto {Id} '{Nome}': '{Prima}' non convertibile",
                    prod.Id, prod.Nome, prima);
            }
            else if (modificato)
            {
                result.TotaleModificati++;
                Logger.LogInformation(
                    "[NormalizzaPrezzi] prodotto {Id} '{Nome}': '{Prima}' -> '{Dopo}'",
                    prod.Id, prod.Nome, prima, dopo);

                prod.Prezzo = dopo;
                await _prodottoRepository.UpdateAsync(prod, autoSave: true);
            }
        }

        Logger.LogInformation(
            "[NormalizzaPrezzi] Completato. Esaminati={E}, Modificati={M}, Anomalie={A}",
            result.TotaleEsaminati, result.TotaleModificati, result.TotaleAnomalie);

        return result;
    }
}