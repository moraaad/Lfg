    public ProdottiAppService(IProdottoRepository prodottoRepository, ProdottoManager prodottoManager, IDistributedCache<ProdottoDownloadTokenCacheItem, string> downloadTokenCache, IRepository<LFG.Categorie.Categoria, Guid> categoriaRepository, IRepository<LFG.Collezioni.Collezione, Guid> collezioneRepository) : base(prodottoRepository, prodottoManager, downloadTokenCache, categoriaRepository, collezioneRepository)
    {
    }
